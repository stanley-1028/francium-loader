    @Test
    void testRankCandidates() {
        CompatibilityPredictor predictor = new CompatibilityPredictor();

        MethodSignature source = new MethodSignature(
            "com/example/FranciumTestBlock",
            "getBlockState",
            "(Lnet/minecraft/core/BlockPos;)Lnet/minecraft/world/level/block/state/BlockState;"
        );

        List<MethodSignature> candidates = List.of(
            new MethodSignature("com/example/FranciumTestBlock", "getBlockState",
                "(Lnet/minecraft/core/BlockPos;)Lnet/minecraft/world/level/block/state/BlockState;"),
            new MethodSignature("com/example/FranciumTestBlock", "m_8055_",
                "(Lnet/minecraft/core/BlockPos;)Lnet/minecraft/world/level/block/state/BlockState;"),
            new MethodSignature("com/example/OtherBlock", "getState",
                "(Lnet/minecraft/core/BlockPos;)Lnet/minecraft/world/level/block/state/BlockState;")
        );

        MethodSignature best = predictor.rankCandidates(source, candidates, "1.20.4", "1.21");
        assertNotNull(best, "Should find a best candidate");
        assertEquals("getBlockState", best.name(),
            "Best candidate should be the exact match");
    }

    @Test
    void testFullResolutionPipelineWithSynthesizedBytecode() {
        // This test simulates the full pipeline: generate bytecode,
        // extract signatures, and verify cross-version compatibility

        // Generate 1.20.4-style bytecode
        byte[] bytecode1204 = createMinecraftLikeClass(
            "com/example/FranciumTestBlock",
            "(Lnet/minecraft/core/BlockPos;)Lnet/minecraft/world/level/block/state/BlockState;",
            "(Lnet/minecraft/core/BlockPos;Z)Z", // 1.20.4 style
            true // Use obfuscated names
        );

        // Generate 1.21-style bytecode
        byte[] bytecode121 = createMinecraftLikeClass(
            "com/example/FranciumTestBlock",
            "(Lnet/minecraft/core/BlockPos;)Lnet/minecraft/world/level/block/state/BlockState;",
            "(Lnet/minecraft/core/BlockPos;Lnet/minecraft/world/level/block/state/BlockState;I)Z", // 1.21 style
            false // Use named methods
        );

        assertNotNull(bytecode1204, "1.20.4 bytecode must be generated");
        assertNotNull(bytecode121, "1.21 bytecode must be generated");

        // Parse and verify both versions are valid
        org.objectweb.asm.ClassReader cr1204 = new org.objectweb.asm.ClassReader(bytecode1204);
        org.objectweb.asm.ClassReader cr121 = new org.objectweb.asm.ClassReader(bytecode121);

        assertEquals("com/example/FranciumTestBlock", cr1204.getClassName(),
            "Class name must match");
        assertEquals("com/example/FranciumTestBlock", cr121.getClassName(),
            "Class name must match");

        // Verify cross-version compatibility prediction works on synthesized bytecode
        CompatibilityPredictor predictor = new CompatibilityPredictor();

        // The unchanged method (getDescriptionId) should still match
        MethodSignature desc1204 = new MethodSignature(
            "com/example/FranciumTestBlock",
            "getDescriptionId",
            "()Ljava/lang/String;"
        );
        MethodSignature desc121 = new MethodSignature(
            "com/example/FranciumTestBlock",
            "getDescriptionId",
            "()Ljava/lang/String;"
        );

        float confDesc = predictor.confidence(desc1204, desc121);
        assertTrue(confDesc >= 0.75f,
            "Unchanged getDescriptionId must have high confidence, got " + confDesc);

        // The obfuscated setBlock (1.20.4) vs named setBlock (1.21) with different signature
        MethodSignature set1204 = new MethodSignature(
            "com/example/FranciumTestBlock",
            "m_67890_",
            "(Lnet/minecraft/core/BlockPos;Z)Z"
        );
        set1204.setMojangName("setBlock");

        MethodSignature set121 = new MethodSignature(
            "com/example/FranciumTestBlock",
            "setBlock",
            "(Lnet/minecraft/core/BlockPos;Lnet/minecraft/world/level/block/state/BlockState;I)Z"
        );

        float confSetBlock = predictor.confidence(set1204, set121);
        assertTrue(confSetBlock > 0.15f,
            "Cross-version setBlock must have some structural similarity, got " + confSetBlock);
    }
