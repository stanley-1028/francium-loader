    @Test
    void testReinforcementLearningWithBytecode() {
        CompatibilityPredictor predictor = new CompatibilityPredictor();

        MethodSignature obfuscated = new MethodSignature(
            "com/example/FranciumTestBlock",
            "m_67890_",
            "(Lnet/minecraft/core/BlockPos;Lnet/minecraft/world/level/block/state/BlockState;I)Z"
        );
        obfuscated.setMojangName("setBlock");

        MethodSignature named = new MethodSignature(
            "com/example/FranciumTestBlock",
            "setBlock",
            "(Lnet/minecraft/core/BlockPos;Lnet/minecraft/world/level/block/state/BlockState;I)Z"
        );

        float before = predictor.confidence(obfuscated, named);

        // Record successful matches (RL learning)
        for (int i = 0; i < 5; i++) {
            predictor.recordSuccessfulMatch(obfuscated, named);
        }

        float after = predictor.confidence(obfuscated, named);
        System.out.println("  RL: before=" + before + ", after=" + after);

        assertTrue(after >= before,
            "Reinforcement learning should not decrease confidence");
    }

